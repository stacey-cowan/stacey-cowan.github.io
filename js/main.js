
//Portfolio Mobile

var desc1 = $('.desc1');

var portfolioPiece1 = $('.portfolio-piece1');

var portfolioPiece1ClickHandler = function() {
    
    var isActive = desc1.attr('data-state');
    
    if (isActive == 'active') {
        desc1.attr('data-state', 'inactive');
    } else {
        desc1.attr('data-state', 'active');    
    }
    
    
};

portfolioPiece1.on('click', portfolioPiece1ClickHandler);


